// fizzBuzz ////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function fizzBuzz() {
  for (let i = 1; i < 16; i++) {
    if (!(i % 3 || i % 5)) {
      console.log("fizzbuzz")
    } else if (!(i % 3)) {
      console.log('fizz')
    } else if (!(i % 5)) {
      console.log("buzz")
    } else {
      console.log(i)
    }
  }
}

////////////////////////////////////////////////////////////////////////////////
// drawChessboard //////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function drawChessboard(rows) { //should take in a parameter representing rows
  var board = "";
  
  for (var y = 0; y < rows; y++) {   
    for (var x = 0; x < rows; x++) {
      if ((x + y) % 2 == 0)
        board += " ";
      else
        board += "#";
    }
    board += "\n";
  }
  
  console.log(board);
}

